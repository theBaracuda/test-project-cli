public class Person{
	private String name;
	private String city;
	private Integer age;

	public String getName(){
		return this.name;
	}
	public String getCity(){
		return this.city;
	}
	public Integer getAge(){
		return this.age;
	}	
	public void setName(String name){
		this.name = name;
	}
	public void setCity(String city){
		this.city = city;
	}
	public void setAge(Integer age){
		this.age = age;
	}
}
